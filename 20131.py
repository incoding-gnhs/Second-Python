이수자 = int(input("이수자 수를 입력하세요: "))

석차 = int(input("석차를 입력하세요: "))

비율 = (석차 / 이수자) * 100

if 비율 <= 4:

      print("1등급입니다")

if 4 < 비율 <= 11:

      print("2등급입니다")

if 11 < 비율 <= 23:

      print("3등급입니다")

if 23 < 비율 <= 40:

      print("4등급입니다")

if 40 < 비율 <= 60:

      print("5등급입니다")

if 60 < 비율 <= 77:

      print("6등급입니다")

